// 10 Operator Perbandingan
package main

import "fmt"

// operator perbandingan udah pasti hasilnya true or false
func operatorPerbandingan() {
	a := 5
	b := 10

	fmt.Println("a < b:", a < b)   // operasi kurang dari
	fmt.Println("a > b:", a > b)   // operasi lebih dari
	fmt.Println("a == b:", a == b) // operasi sama dengan
	fmt.Println("a != b:", a != b) // operasi tidak sama dengan
	fmt.Println("a <= b:", a <= b) // operasi kurang dari sama dengan
	fmt.Println("a >= b:", a >= b) // operasi lebih dari sama dengan
}
